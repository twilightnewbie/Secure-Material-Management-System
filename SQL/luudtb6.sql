    p_pdf_signed IN BLOB,
    p_signature IN CLOB,
    p_signed_by IN VARCHAR2
) AS
    v_hash RAW(32);
BEGIN
    -- Tính toán Hash t?i t?ng DB đ? b?o v? d? li?u
