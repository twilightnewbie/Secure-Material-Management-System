-- Hŕm ki?m tra file có b? s?a đ?i trong DB hay không
CREATE OR REPLACE FUNCTION VERIFY_FILE_INTEGRITY (p_id IN NUMBER) 
RETURN VARCHAR2 AS
    v_blob BLOB;
    v_hash_db RAW(32);
    v_hash_now RAW(32);
BEGIN
    SELECT FILE_DATA, FILE_HASH INTO v_blob, v_hash_db 
    FROM FILE_SIGN WHERE ID = p_id;

    v_hash_now := FUN_HASH_BLOB(v_blob);

    IF v_hash_now = v_hash_db THEN
        RETURN 'FILE TOAN VEN - KHONG BI SUA DOI';
    ELSE
        RETURN 'FILE DA BI THAY DOI TRONG DATABASE';
    END IF;
END;
