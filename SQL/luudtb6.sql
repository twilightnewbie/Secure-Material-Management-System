CREATE OR REPLACE PACKAGE RSA AS
    FUNCTION encrypt(p_plainText VARCHAR2, p_publicKey VARCHAR2) RETURN VARCHAR2;
    FUNCTION decrypt(p_encryptText VARCHAR2, p_privateKey VARCHAR2) RETURN VARCHAR2;
END RSA;
/

CREATE OR REPLACE PACKAGE BODY RSA AS

    FUNCTION encrypt(p_plainText VARCHAR2, p_publicKey VARCHAR2) RETURN VARCHAR2 IS
        v_raw RAW(2000);
        v_encoded VARCHAR2(4000);
    BEGIN
        -- Mô ph?ng RSA: n?i key vŕo text + m? hóa Base64 (cho ??p)
        v_raw := UTL_RAW.CAST_TO_RAW(p_publicKey || ':' || p_plainText);
        v_encoded := UTL_ENCODE.BASE64_ENCODE(v_raw);
        RETURN v_encoded;
    END encrypt;

    FUNCTION decrypt(p_encryptText VARCHAR2, p_privateKey VARCHAR2) RETURN VARCHAR2 IS
        v_raw RAW(2000);
        v_decoded VARCHAR2(4000);
        v_text VARCHAR2(4000);
    BEGIN
        -- Gi?i m? Base64
        v_raw := UTL_ENCODE.BASE64_DECODE(UTL_RAW.CAST_TO_RAW(p_encryptText));
        v_decoded := UTL_RAW.CAST_TO_VARCHAR2(v_raw);

        -- C?t b? ph?n key gi? l?p
        v_text := SUBSTR(v_decoded, INSTR(v_decoded, ':') + 1);
        RETURN v_text;
    END decrypt;

END RSA;
