CREATE OR REPLACE FUNCTION RSA_SIGN_HASH (
    p_hash IN RAW,
    p_private_key IN CLOB
) RETURN CLOB
AS
    v_sig_raw RAW(32767);
BEGIN
    v_sig_raw := DBMS_CRYPTO.ENCRYPT(
        src => p_hash,
        typ => DBMS_CRYPTO.encrypt_rsa
               + DBMS_CRYPTO.chain_rsa_pkcs1,
        key => p_private_key
    );

    RETURN UTL_RAW.cast_to_varchar2(
        UTL_ENCODE.base64_encode(v_sig_raw)
    );
END;
/
CREATE OR REPLACE FUNCTION HASH_SHA256 (
    p_data IN BLOB
) RETURN RAW
AS
BEGIN
    RETURN DBMS_CRYPTO.HASH(
        p_data,
        DBMS_CRYPTO.HASH_SH256
    );
END;
