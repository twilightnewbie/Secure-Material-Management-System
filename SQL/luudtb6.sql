-- Hŕm ki?m tra ch? k? RSA (Mô ph?ng băm đ?i chi?u)
CREATE OR REPLACE FUNCTION RSA_VERIFY (
    p_data IN CLOB,
    p_signature_hash IN RAW
) RETURN NUMBER AS
BEGIN
    IF HASH_SHA256_TEXT(p_data) = p_signature_hash THEN
        RETURN 1; -- H?p l?
    ELSE
        RETURN 0; -- Không h?p l?
    END IF;
END;
