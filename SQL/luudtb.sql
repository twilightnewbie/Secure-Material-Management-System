CREATE OR REPLACE PROCEDURE GET_USER_DEVICES (
    p_username IN VARCHAR2,
    p_result OUT SYS_REFCURSOR
)
AS
BEGIN
    OPEN p_result FOR
        SELECT ID, DEVICE_ID, DEVICE_NAME,
               REGISTERED_AT, LAST_ACTIVE_AT, IS_REVOKED
        FROM USER_DEVICE
        WHERE USERNAME = p_username
        ORDER BY REGISTERED_AT DESC;
END;
/
CREATE OR REPLACE PROCEDURE REVOKE_DEVICE (
    p_device_id IN NUMBER
)
AS
BEGIN
    UPDATE USER_DEVICE
    SET IS_REVOKED = 'Y'
    WHERE ID = p_device_id;

    COMMIT;
END;
/
