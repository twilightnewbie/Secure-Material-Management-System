CREATE TABLE FILE_SIGN (
  ID             NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
  DOC_TYPE       VARCHAR2(50),        -- HOADON, PHIEUXUAT, ...
  DOC_ID         VARCHAR2(100),       -- id hóa đőn g?c
  FILENAME       VARCHAR2(512),
  CONTENT_TYPE   VARCHAR2(100),
  FILE_DATA      BLOB,                -- file g?c (pdf/docx) ho?c file đ? k?
  SIGNATURE_P7S   BLOB,               -- PKCS7 detached/attached signature
  SIGNER_CERT     BLOB,               -- signer's certificate (optional)
  SIGNED_AT      TIMESTAMP DEFAULT SYSTIMESTAMP,
  SIGNED_BY      VARCHAR2(100)
);

-- B?ng thi?t b? đăng nh?p
CREATE TABLE USER_DEVICE (
  ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
  USERNAME VARCHAR2(100),
  DEVICE_ID VARCHAR2(200), 
  DEVICE_NAME VARCHAR2(200),
  REGISTERED_AT TIMESTAMP DEFAULT SYSTIMESTAMP,
  LAST_ACTIVE_AT TIMESTAMP,
  IS_REVOKED CHAR(1) DEFAULT 'N'
);

-- B?ng lýu log g?i mail / tr?ng thái
CREATE TABLE FILE_SIGN_LOGIN (
  ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
  FILE_SIGN_ID NUMBER,
  ACTION VARCHAR2(50), -- SIGNED, SENT, VERIFIED
  ACTION_BY VARCHAR2(100),
  ACTION_AT TIMESTAMP DEFAULT SYSTIMESTAMP,
  DETAIL VARCHAR2(4000)
);
CREATE OR REPLACE PROCEDURE GET_INVOICE_PDF (
  p_invoice_id IN VARCHAR2,
  p_pdf_out OUT BLOB,
  p_filename OUT VARCHAR2
) AS
BEGIN
  SELECT FILE_PDF, FILENAME INTO p_pdf_out, p_filename
  FROM HOADON
  WHERE INVOICE_ID = p_invoice_id;
EXCEPTION
  WHEN NO_DATA_FOUND THEN
    p_pdf_out := EMPTY_BLOB();
    p_filename := NULL;
END;
