-- [4.2] Qu?n l? Session & Thi?t b? (C?p nh?t dùng LOGIN_DEVICES)
CREATE OR REPLACE PROCEDURE SET_SESSION (p_username IN VARCHAR2, p_session IN VARCHAR2, p_device IN VARCHAR2) AS
BEGIN
    MERGE INTO APP_SESSION s
    USING (SELECT p_username username FROM dual) x
    ON (s.USERNAME = x.username)
    WHEN MATCHED THEN
        UPDATE SET SESSION_ID = p_session, DEVICE_NAME = p_device, LOGIN_TIME = SYSTIMESTAMP
    WHEN NOT MATCHED THEN
        INSERT (USERNAME, SESSION_ID, DEVICE_NAME, LOGIN_TIME) VALUES (p_username, p_session, p_device, SYSTIMESTAMP);
END;
/

CREATE OR REPLACE FUNCTION CHECK_SESSION (p_username IN VARCHAR2, p_session IN VARCHAR2) RETURN NUMBER AS
    v_sess VARCHAR2(100);
BEGIN
    SELECT SESSION_ID INTO v_sess FROM APP_SESSION WHERE USERNAME = p_username;
    IF v_sess = p_session THEN RETURN 1; ELSE RETURN 0; END IF;
EXCEPTION WHEN NO_DATA_FOUND THEN RETURN 0;
END;
/

CREATE OR REPLACE PROCEDURE GET_USER_DEVICES (p_username IN VARCHAR2, p_result OUT SYS_REFCURSOR) AS
BEGIN
    OPEN p_result FOR SELECT ID, DEVICE_ID, DEVICE_NAME, REGISTERED_DATE, LAST_ACTIVE, IS_REVOKED 
    FROM LOGIN_DEVICES WHERE USERNAME = p_username AND IS_REVOKED = 0;
END;
/

-- [4.3] X? l? Bãm (Hash) & Ch? k? s?
CREATE OR REPLACE FUNCTION FUN_HASH_BLOB (p_blob IN BLOB) RETURN RAW AS
BEGIN
    RETURN DBMS_CRYPTO.HASH(p_blob, DBMS_CRYPTO.HASH_SH256);
END;
/

CREATE OR REPLACE FUNCTION HASH_SHA256_TEXT (p_text IN CLOB) RETURN RAW AS
BEGIN
    RETURN DBMS_CRYPTO.HASH(p_text, DBMS_CRYPTO.HASH_SH256);
END;
/

CREATE OR REPLACE FUNCTION HASH_SHA256 (p_data IN BLOB) RETURN RAW AS
BEGIN
    RETURN DBMS_CRYPTO.HASH(p_data, DBMS_CRYPTO.HASH_SH256);
END;
/

CREATE OR REPLACE FUNCTION RSA_VERIFY (p_data IN CLOB, p_signature_hash IN RAW) RETURN NUMBER AS
BEGIN
    IF HASH_SHA256_TEXT(p_data) = p_signature_hash THEN RETURN 1; ELSE RETURN 0; END IF;
END;
/

CREATE OR REPLACE FUNCTION RSA_SIGN_HASH (p_hash IN RAW, p_private_key IN CLOB) RETURN CLOB AS
    v_sig_raw RAW(32767);
BEGIN
    v_sig_raw := DBMS_CRYPTO.ENCRYPT(
        src => p_hash,
        typ => DBMS_CRYPTO.encrypt_rsa + DBMS_CRYPTO.chain_rsa_pkcs1,
        key => p_private_key
    );
    RETURN UTL_RAW.cast_to_varchar2(UTL_ENCODE.base64_encode(v_sig_raw));
END;
/

