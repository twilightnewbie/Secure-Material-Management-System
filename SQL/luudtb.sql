-- [3.1] Qu?n l? Quy?n User
CREATE OR REPLACE PACKAGE ADMIN_USER_MGR AS
    PROCEDURE GRANT_PRIV(p_user IN VARCHAR2, p_table IN VARCHAR2, p_privs IN VARCHAR2);
    PROCEDURE REVOKE_PRIV(p_user IN VARCHAR2, p_table IN VARCHAR2, p_privs IN VARCHAR2);
    PROCEDURE LIST_PRIVS(p_user IN VARCHAR2, p_cur OUT SYS_REFCURSOR);
END ADMIN_USER_MGR;
/

CREATE OR REPLACE PACKAGE BODY ADMIN_USER_MGR AS
    PROCEDURE GRANT_PRIV(p_user IN VARCHAR2, p_table IN VARCHAR2, p_privs IN VARCHAR2) IS
        v_sql VARCHAR2(4000);
    BEGIN
        v_sql := 'GRANT ' || p_privs || ' ON ' || p_table || ' TO ' || p_user;
        EXECUTE IMMEDIATE v_sql;
    END;

    PROCEDURE REVOKE_PRIV(p_user IN VARCHAR2, p_table IN VARCHAR2, p_privs IN VARCHAR2) IS
        v_sql VARCHAR2(4000);
    BEGIN
        v_sql := 'REVOKE ' || p_privs || ' ON ' || p_table || ' FROM ' || p_user;
        EXECUTE IMMEDIATE v_sql;
    END;

    PROCEDURE LIST_PRIVS(p_user IN VARCHAR2, p_cur OUT SYS_REFCURSOR) IS
    BEGIN
        OPEN p_cur FOR SELECT TABLE_NAME, PRIVILEGE FROM DBA_TAB_PRIVS WHERE GRANTEE = UPPER(p_user) ORDER BY TABLE_NAME;
    END;
END ADMIN_USER_MGR;
/

