CREATE OR REPLACE PROCEDURE SAVE_SIGNED_PDF_SECURE (
    p_doc_id IN VARCHAR2, p_filename IN VARCHAR2, p_pdf_signed IN BLOB, 
    p_signature IN CLOB, p_signed_by IN VARCHAR2
) AS
    v_hash RAW(32);
BEGIN
    v_hash := FUN_HASH_BLOB(p_pdf_signed);
    INSERT INTO FILE_SIGN (DOC_TYPE, DOC_ID, FILENAME, CONTENT_TYPE, FILE_DATA, FILE_HASH, SIGNATURE_P7S, SIGNED_BY)
    VALUES ('HOADON', p_doc_id, p_filename, 'application/pdf', p_pdf_signed, v_hash, p_signature, p_signed_by);
    COMMIT;
END;
/

CREATE OR REPLACE FUNCTION VERIFY_FILE_INTEGRITY (p_id IN NUMBER) RETURN VARCHAR2 AS
    v_blob BLOB;
    v_hash_db RAW(32);
    v_hash_now RAW(32);
BEGIN
    SELECT FILE_DATA, FILE_HASH INTO v_blob, v_hash_db FROM FILE_SIGN WHERE ID = p_id;
    v_hash_now := FUN_HASH_BLOB(v_blob);
    IF v_hash_now = v_hash_db THEN RETURN 'FILE TOAN VEN - KHONG BI SUA DOI';
    ELSE RETURN 'FILE DA BI THAY DOI TRONG DATABASE'; END IF;
END;
/

